// server.js - OpenAI to NVIDIA NIM API Proxy
const express = require('express');
const cors = require('cors');
const axios = require('axios');
const axiosRetry = require('axios-retry');

const app = express();
const PORT = process.env.PORT || 3000;

// ðŸ”¥ TIMEOUT MIDDLEWARE - Prevent freezing on long responses
const timeout = require('connect-timeout');
app.use(timeout('180s')); // 3 minutes timeout for reasoning-heavy responses
app.use((req, res, next) => {
  if (!req.timedout) next();
  else {
    res.status(504).json({ error: { message: 'Request timeout', code: 504 } });
  }
});

// Middleware - Increased limits for large payloads
app.use(cors());
app.use(express.json({ limit: '100mb', extended: true }));
app.use(express.urlencoded({ limit: '100mb', extended: true }));

// ðŸ”¥ AXIOS RETRY CONFIGURATION - Handle network issues
axiosRetry(axios, { 
  retries: 3, 
  retryDelay: axiosRetry.exponentialDelay,
  retryCondition: (error) => {
    return axiosRetry.isNetworkOrIdempotentRequestError(error) || 
           error.response?.status === 429 || 
           error.response?.status === 503 ||
           error.code === 'ECONNRESET' ||
           error.code === 'ETIMEDOUT';
  }
});

